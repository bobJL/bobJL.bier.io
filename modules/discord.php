<h1>BIERPROEVERIJEN</h1>
<p><span style="color: #e63939;"><strong>De Bierverteller</strong></span> heeft een hart voor bier. Van proeverijen maakt hij een echte beleving.<br>
Hij heeft namelijk net zo veel verstand van bier als van verhalen.</p>
<p><span style="color: #e63939;"><strong>De Bierverteller</strong></span> neemt je mee in de rijke biercultuur. Van brewpub&nbsp;tot brouwerij, van&nbsp;I.P.A.<br>
tot Stout en van hop naar mout in proefglaasjes van 12 centiliter.</p>
<p><span style="color: #e63939;"><strong>Proeverijen</strong></span> in de bierboetiek of op locatie.</p>
<p><span style="color: #e63939;"><strong>De Bierverteller</strong></span> aan boord.<br>
De Bierverteller vaart ook op de Utrechtse grachten!<br>
Kies voor één van zijn arrangementen op een sloep van Varen in Utrecht voor een bijzondere proeverij.<br>
Op basis van beschikbaarheid.</p>
<p><span style="color: #e63939;"><strong>Vrijblijvende offertes</strong></span> op aanvraag via <a href="mailto:info@debierverteller.nl" target="_blank" rel="noopener noreferrer">info@debierverteller.nl</a></p>
<p><span style="color: #e63939;"><strong>Openbare</strong></span> proeverij? Aanschuiven? Geen gezelschap?<br>
Schuif aan bij een proeverij op inschrijving. Kijk in de <a href="http://www.debierverteller.nl/#section_agenda">agenda</a></p>
